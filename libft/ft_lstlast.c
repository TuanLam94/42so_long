#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*elem;

	elem = lst;
	if (elem == NULL)
	{
		return (NULL);
	}
	while (elem->next != NULL)
		elem = elem->next;
	return (elem);
}
